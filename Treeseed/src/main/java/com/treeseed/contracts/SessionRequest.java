package com.treeseed.contracts;

public class SessionRequest {

	private int id;
	private String role;
	
	public SessionRequest() {
		super();
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}

}
